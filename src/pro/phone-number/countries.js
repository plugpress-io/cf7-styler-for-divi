export function flagEmoji(iso2) {
	if (!iso2 || iso2.length !== 2) return '';
	const a = 0x1f1e6 - 65;
	return String.fromCodePoint(
		...[...iso2.toUpperCase()].map((c) => a + c.charCodeAt(0))
	);
}
